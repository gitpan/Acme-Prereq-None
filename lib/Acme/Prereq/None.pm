=head1 NAME

Acme::Prereq::None - Module for testing CPAN module prerequisites

=head1 SYNOPSIS

    use Acme::Prereq::None;

=head1 DESCRIPTION

Acme::Prereq::None does nothing and it has no dependencies.
It can be used for testing dependencies among CPAN modules.

